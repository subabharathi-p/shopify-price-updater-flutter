import 'package:flutter/material.dart';
import 'package:firebase_core/firebase_core.dart';
import 'package:hive_flutter/hive_flutter.dart';
import 'services/db_services.dart';
import 'screen/splash_screen.dart';
import 'screen/analytics_dashboard_screen.dart'; // âœ… Added import
import 'firebase_options.dart'; // make sure this file exists (auto-generated by Firebase CLI)

Future<void> main() async {
  WidgetsFlutterBinding.ensureInitialized();

  bool initError = false;
  try {
    // ðŸ”¹ Initialize Firebase first
    await Firebase.initializeApp(
      options: DefaultFirebaseOptions.currentPlatform,
    );

    // ðŸ”¹ Then initialize Hive (for local logs)
    await Hive.initFlutter();
    await DBService.initDB();

    debugPrint("âœ… Firebase & Hive initialized successfully");
  } catch (e) {
    debugPrint("âŒ Initialization failed: $e");
    initError = true;
  }

  runApp(MyApp(initError: initError));
}

class MyApp extends StatelessWidget {
  final bool initError;
  const MyApp({super.key, this.initError = false});

  @override
  Widget build(BuildContext context) {
    return MaterialApp(
      debugShowCheckedModeBanner: false,
      title: 'Shopify PriceSync',
      theme: ThemeData(
        primarySwatch: Colors.purple,
        scaffoldBackgroundColor: const Color(0xFFF8EDEE),
      ),
      home: initError ? const InitErrorScreen() : const SplashScreen(),
    );
  }
}

class InitErrorScreen extends StatelessWidget {
  const InitErrorScreen({super.key});

  @override
  Widget build(BuildContext context) {
    return Scaffold(
